package hellow;

public class forLoopDemo {  // Define the class forLoopDemo

    public static void main(String[] args) {  // The main method, the entry point of the program
        // Use a for loop to print numbers from 1 to 10
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);  // Print the current value of i
        }
    }
}